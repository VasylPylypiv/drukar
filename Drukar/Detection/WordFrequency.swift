    static func rank(of word: String, language: String) -> Int? {
        let lowered = word.lowercased()
        if language == "uk" { return ukrainianRanks[lowered] }
        if language == "en" { return englishRanks[lowered] }
        return nil
    }

    static func score(of word: String, language: String) -> Double {
        guard let rank = rank(of: word, language: language) else { return 0.0 }
        return 1.0 - (Double(rank) / 501.0)
    }

